    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            // TODO add your handling code here:
            
            File archivo = new File("archivo.txt");
            PrintWriter escribir;
        try {
            escribir = new PrintWriter(archivo);
            escribir.print(TXT1.getText());
            escribir.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        try {
            Reader lector = new BufferedReader (new FileReader ("archivo.txt"));
            Lexico lexico = new Lexico(lector);
            String resultado = "";
            while (true) {
                Tokens tokens = lexico.yylex();
                if (tokens == null) {
                    resultado+="\n\nFin del analisis";
                    txtArea.setText(resultado);
                    return;
                }
                switch (tokens) {
                    case ERROR:
                        resultado+= "ERROR al analizar un simbolo que no existe.";
                                             
                        break;
                    case Identificador: case Numero: case Reserve:
                        resultado+= lexico.lexema + ": => "+tokens+"\n";
                    default:
                        resultado +="\n";
                        break;
                }
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
            
           
    }//GEN-LAST:event_jButton1ActionPerformed

